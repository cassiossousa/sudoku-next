import { SudokuGrid } from "./backtracking";

describe('Base Sudoku', () => {
    it('prints the Sudoku properly', () => {
        const sudoku = new SudokuGrid([
            [6, 0, 2, 4, 1, 0, 0, 0, 8],
            [0, 1, 5, 7, 0, 3, 0, 0, 9],
            [7, 3, 4, 0, 0, 8, 0, 6, 0],
            [5, 0, 3, 2, 0, 4, 0, 0, 0],
            [0, 0, 8, 0, 9, 0, 0, 0, 5],
            [0, 0, 6, 0, 5, 7, 0, 3, 0],
            [0, 5, 0, 0, 7, 0, 9, 0, 4],
            [4, 0, 9, 0, 0, 0, 2, 5, 0],
            [0, 8, 7, 0, 0, 0, 6, 0, 3],
        ]);

        expect(sudoku.print()).toBe(
            `-------------\n` +
            `|6 2|41 |  8|\n` +
            `| 15|7 3|  9|\n` +
            `|734|  8| 6 |\n` +
            `-------------\n` +
            `|5 3|2 4|   |\n` +
            `|  8| 9 |  5|\n` +
            `|  6| 57| 3 |\n` +
            `-------------\n` +
            `| 5 | 7 |9 4|\n` +
            `|4 9|   |25 |\n` +
            `| 87|   |6 3|\n` +
            `-------------`
        );
    });
});